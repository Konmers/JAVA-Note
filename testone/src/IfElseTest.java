/*
 * 分支结构
 *  If - else 
 *  
 *  
 * 格式
 * if(条件表达式1){
 * }
 * else{
 * }
 * 
 * if(条件表达式1){
 * }
 * else if(条件表达式2){
 * }
 * else{
 * }
 */
public class IfElseTest {
	public static void main(String[] args) {
		int age = 11;
		if(age>=10) {
			System.out.println("1");
		}else if(age>=20) {
			System.out.println("2");
		}else if(age>=50) {
			System.out.println("3");
		}else if(age>=60) {
			System.out.println("4");
		}
		
		//
			
	}
}
