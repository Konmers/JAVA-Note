/*
	赋值运算符
	
	=  += ， -= ， *= ， /= ， %= ，==
*/
public class SetValueTest {
	public static void main(String[] arguments) {
		
//		赋值
		int a;
		int b1=10;
		int c=10;
		int d=10;
		int e=10;
		int f=10;
		//连续赋值
		a=e=f=c=10;
		
		c+=a;//c=c+a
		
		b1-=c;//b=b-c
		
		d*=a;//d=d*a
		
		e/=a;//d=d/a
		
		f%=a;//d=d%a
		
		
	}
}
